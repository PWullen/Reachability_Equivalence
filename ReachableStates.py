def Constrain(f, g):
    """valid generalized cofactor is generated by reduction of bdd via constrain operator

       :param f: ROBDD f
       :param g: ROBDD g
       :return: constrained fvg"""
    x = BDDNode
    if g == 0 or f == 0 or f == 1:
        return f
    elif f == g:
        return 1
    elif f ==  ~g or g == 0:
        return 0
    elif g.x == 0:
        return Constrain(~f.x, ~g.x)
    elif ~g.x == 0:
        return Constrain(f.x, g.x)
    else:
        return ITE(x, Constrain(f.x, g.x), Constrain(~f.x, ~g.x))


